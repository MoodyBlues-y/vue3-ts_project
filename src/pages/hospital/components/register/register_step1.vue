<script setup lang="ts">
import { reqBookingSchedule, reqHospitalDoctor } from "@/api/hospital";
import { computed, onMounted, ref } from "vue";
import { useRoute, useRouter } from "vue-router";
import type {
  HospitalWordData,
  DoctorResponseData,
  DocArr,
  Doctor,
} from "@/api/hospital/type";

let $route = useRoute();
let $router = useRouter();
let pageNo = ref<number>(1);
let limit = ref<number>(6);
let workData = ref<any>({});
//存储第一天的数据进行展示
let workTime = ref({});
let docArr = ref<DocArr>([]);

onMounted(() => {
  fetchWorkData();
});

const fetchWorkData = async () => {
  let result: HospitalWordData = await reqBookingSchedule(
    pageNo.value,
    limit.value,
    $route.query.hoscode,
    $route.query.depcode
  );
  if (result.code == 200) {
    workData.value = result.data;
    workTime.value = workData.value.bookingScheduleList[0].workDate;
    getWorkData();
  }
};

const getWorkData = async () => {
  let hoscode: string = $route.query.hoscode;
  let depcode: string = $route.query.depcode;
  let workDate: string = workTime.value.workDate;
  let result: DoctorResponseData = await reqHospitalDoctor(
    hoscode,
    depcode,
    workDate
  );
  if (result.code == 200) {
    docArr.value = result.data;
  }
};

const changeItem = (item: any) => {
  workTime.value = item;
  getWorkData();
};

//计算出上午排班医生数据
let morningArr = computed(() => {
  return docArr.value.filter((doc: Doctor) => {
    return doc.workTime == 0;
  });
});

//计算出下午排班医生数据
let afternoonArr = computed(() => {
  return docArr.value.filter((doc: Doctor) => {
    return doc.workTime == 1;
  });
});

const goStep2 = (doctor: any) => {
  $router.push({
    path: "/hospital/register_step2",
    query: { docId: doctor.id },
  });
};
</script>
